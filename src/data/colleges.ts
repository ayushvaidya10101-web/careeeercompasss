// Helper functions for fast data access
export function getCollegesByCountry(countryId: string): College[] {
  return COLLEGES_DATABASE
    .filter(college => college.country === countryId)
    .sort((a, b) => a.qsRank - b.qsRank);
}

export function getCollegeById(id: string): College | undefined {
  return COLLEGES_DATABASE.find(college => college.id === id);
}

export function getTopCollegesGlobal(limit: number = 20): College[] {
  return [...COLLEGES_DATABASE]
    .sort((a, b) => a.qsRank - b.qsRank)
    .slice(0, limit);
}

export function getCollegesForCareer(careerTags: string[]): College[] {
  // Match colleges that offer courses related to career tags
  return COLLEGES_DATABASE.filter(college =>
    college.popularCourses.some(course =>
      careerTags.some(tag =>
        course.toLowerCase().includes(tag.toLowerCase()) ||
        tag.toLowerCase().includes(course.toLowerCase())
      )
    )
  ).sort((a, b) => a.qsRank - b.qsRank);
}
